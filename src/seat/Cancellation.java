    private void ContinueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ContinueButtonActionPerformed
        String passportNo = PassportNoField.getText();
        if (PassportNoField.getText().isEmpty()) {
            WarningText1.setText("* Passport No. is required");
        } else if (passportNo.equals(flight.getSeatPassportNo(getSeat()))) {
            WarningText1.setText("");
            flight.cancelSeat(getSeat());
            System.out.println("A Seat : " + getSeat() + " has been successfully cancelled.");
            JOptionPane.showMessageDialog(this, "A seat " + getSeat() + " has been successfully cancelled.", "", JOptionPane.PLAIN_MESSAGE);
            this.setVisible(false);
            new main().setVisible(true);
        } else {
            WarningText1.setText("");
            JOptionPane.showMessageDialog(this, "Incorrect Passport No.", "", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ContinueButtonActionPerformed

